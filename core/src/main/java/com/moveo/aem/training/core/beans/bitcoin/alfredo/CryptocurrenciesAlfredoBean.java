package com.moveo.aem.training.core.beans.bitcoin.alfredo;

public class CryptocurrenciesAlfredoBean {

    private String id;
    private String nome;
    private String logo;
    private String sigla;
    private String prezzoUsd;
    private String dataUltimoAggiornamento;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }

    public String getPrezzoUsd() {
        return prezzoUsd;
    }

    public void setPrezzoUsd(String prezzoUsd) {
        this.prezzoUsd = prezzoUsd;
    }

    public String getDataUltimoAggiornamento() {
        return dataUltimoAggiornamento;
    }

    public void setDataUltimoAggiornamento(String dataUltimoAggiornamento) {
        this.dataUltimoAggiornamento = dataUltimoAggiornamento;
    }

}
